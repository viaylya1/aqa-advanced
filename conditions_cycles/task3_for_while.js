const numberFour = 4;
let multiplyResult;

for (let i = 1; i < 11; i++) {
  multiplyResult = numberFour * i;
  console.log(`${numberFour} x ${i} = ${multiplyResult}`);
}

let i = 1;
while (i < 11) {
  multiplyResult = numberFour * i;
  console.log(`${numberFour} x ${i} = ${multiplyResult}`);
  i++;
}
